  static List<ExploreCity> citiesForCountry(String countryId) =>
      cities.where((c) => c.countryId == countryId).toList();

  static List<ExplorePlace> placesForCity(String cityId) =>
      places.where((p) => p.cityId == cityId).toList();

  static List<ExplorePlace> placesForCityAndCategory(
    String cityId,
    String categoryId,
  ) =>
      places
          .where((p) => p.cityId == cityId && p.categoryId == categoryId)
          .toList();

  static ExploreCountry? countryById(String id) {
    try {
      return countries.firstWhere((c) => c.id == id);
    } catch (_) {
      return null;
    }
  }

  static ExploreCity? cityById(String id) {
    try {
      return cities.firstWhere((c) => c.id == id);
    } catch (_) {
      return null;
    }
  }

  static ExplorePlace? placeById(String id) {
    try {
      return places.firstWhere((p) => p.id == id);
    } catch (_) {
      return null;
    }
  }

  static List<ExploreCategory> categoriesForCity(String cityId) {
    final city = cityById(cityId);
    if (city == null) return [];
    return categories
        .where((cat) => city.categoryIds.contains(cat.id))
        .toList();
  }

  static List<ExploreCity> trendingCities() =>
      cities.where((c) => c.isTrending).toList();

  static List<ExploreCountry> featuredCountries() {
    final sorted = List<ExploreCountry>.from(countries);
    sorted.sort((a, b) => b.popularityScore.compareTo(a.popularityScore));
    return sorted;
  }

  static List<ExplorePlace> nearbyPlaces(
    String placeId, {
    int limit = 5,
  }) {
    final place = placeById(placeId);
    if (place == null) return [];
    final nearby = place.nearbyPlaceIds
        .map((id) => placeById(id))
        .whereType<ExplorePlace>()
        .toList();
    // Fill remaining with same-city places sorted by rating if not enough nearby
    if (nearby.length < limit) {
      final sameCityOthers = placesForCity(place.cityId)
          .where(
            (p) => p.id != placeId && !place.nearbyPlaceIds.contains(p.id),
          )
          .toList()
        ..sort((a, b) => b.rating.compareTo(a.rating));
      nearby.addAll(sameCityOthers.take(limit - nearby.length));
    }
    return nearby.take(limit).toList();
  }
